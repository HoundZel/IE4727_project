

<?php
// Database connection
$servername = "localhost";
$username = "root";
$password = "";
$dbname = "professionals";
$conn = new mysqli($servername, $username, $password, $dbname);

if ($conn->connect_error) {
    die("Connection failed: " . $conn->connect_error);
}

// Get current datetime
$currentDateTime = new DateTime();

// Query unsent emails scheduled to be sent at or before the current time
$email_query = $conn->prepare("SELECT id, recipient_email, subject, message FROM scheduled_emails WHERE scheduled_time <= ? AND sent = 0");
$currentTime = $currentDateTime->format("Y-m-d H:i:s");
$email_query->bind_param("s", $currentTime);
$email_query->execute();
$email_result = $email_query->get_result();

while ($email = $email_result->fetch_assoc()) {
    // Send the email
    mail($email['recipient_email'], $email['subject'], $email['message'], 'From: f32ee@localhost');

    // Mark the email as sent
    $update_stmt = $conn->prepare("UPDATE scheduled_emails SET sent = 1 WHERE id = ?");
    $update_stmt->bind_param("i", $email['id']);
    $update_stmt->execute();
    $update_stmt->close();
}

$email_query->close();
$conn->close();
?>























































































































































































































































































